import { getCachedGlobal } from '@/utilities/getGlobals'

export async function SocialMediaIcons() {
  const socialLinks: Socialmedialink = await getCachedGlobal('socialmedialink', 1)()

  return (
    <div className="flex items-center gap-4 w-80 mx-auto md:mx-0">
      <h2>Follow us!</h2>
      {socialLinks.socialMediaLinks?.map((social) => {
        switch (social.socialType) {
          case 'bluesky':
            return (
                <div key={social.id} className="grow socialsvg">
                  <a href={social.url} target="_blank">
                  {blueskyLogo}
                  </a>
                </div>
            )
          case 'telegram':
            return (
                <div key={social.id} className="grow socialsvg">
                  <a href={social.url} target="_blank">
                  {telegramLogo}
                  </a>
                </div>
            )
          case 'github':
            return (
                <div key={social.id} className="grow socialsvg">
                  <a href={social.url} target="_blank">
                  {githubLogo}
                  </a>
                </div>
            )
          case 'discord':
            return (
                <div key={social.id} className="grow socialsvg">
                  <a href={social.url} target="_blank">
                  {discordLogo}
                  </a>
                </div>
            )
        }
      })}
    </div>
  )
}